package com.tisson.webservice.rest.domain;

public class ShortcutTrader  {

	private String perAmount;
	
	private String allamount;
	
	private String alltransaction;

	public String getPerAmount() {
		return perAmount;
	}

	public void setPerAmount(String perAmount) {
		this.perAmount = perAmount;
	}

	public String getAllamount() {
		return allamount;
	}

	public void setAllamount(String allamount) {
		this.allamount = allamount;
	}

	public String getAlltransaction() {
		return alltransaction;
	}

	public void setAlltransaction(String alltransaction) {
		this.alltransaction = alltransaction;
	}
	
	
}
